// =============== インクルード =====================
#include "SceneManager.h"
#include "SceneBase.h"
#include "SceneGameTest.h"


/* ========================================
	コンストラクタ関数
	-------------------------------------
	内容：初期化
=========================================== */
void SceneManager::Init()
{
	ChangeScene<SceneGameTest>();
}

/* ========================================
	デストラクタ関数
	-------------------------------------
	内容：削除時
=========================================== */
void SceneManager::Uninit()
{
	m_pScene->Uninit();	// 終了処理
}


/* ========================================
	更新関数
	-------------------------------------
	内容：更新処理
=========================================== */
void SceneManager::Update()
{
	m_pScene->Update();	// 更新処理
}

/* ========================================
	描画関数
	-------------------------------------
	内容：描画処理
=========================================== */
void SceneManager::Draw()
{
	m_pScene->Draw();	// 描画処理
}


/* ========================================
	シーン変更関数
	-------------------------------------
	内容：シーンを変更する
	-------------------------------------
	戻値：新しいシーンのポインタ
=========================================== */
SceneBase* SceneManager::GetScene()
{
	return m_pScene.get();
}
